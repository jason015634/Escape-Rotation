#pragma once
#include "CObject.h"

// 가장 상단의 UI
// UI는 계층적 구조를 가진다.
// 모든 UI가 해당 클래스를 상속받는다.
class CUI :
	public CObject
{
private:
	vector<CUI*> m_vecChildUI;	// 자식 UI
	CUI*		 m_pParentUI;	// 부모 UI

	Vec2		 m_vFinalPos;	// 최종 위치

	bool		 m_bCamAffected;	// UI가 카메라 영향을 받는지
	bool		 m_bMouseOn;		// UI 위에 마우스가 있는지
	bool		 m_bLbtnDown;		// UI에 왼쪽 버튼이 눌린적이 있는지

public:
	Vec2 GetFinalPos() { return m_vFinalPos; }
	CUI* GetParent() { return m_pParentUI; }
	bool IsMouseOn() { return m_bMouseOn; }
	bool IsLbtnDown() { return m_bLbtnDown; }

	void AddChild(CUI* _pUI) { m_vecChildUI.push_back(_pUI); _pUI->m_pParentUI = this; }
	const vector<CUI*>& GetChildUI() { return m_vecChildUI; }

	void DeleteUI();

public:
	virtual void update();
	virtual void finalupdate();
	virtual void render(HDC _dc);

private:
	void update_child();
	void finalupdate_child();
	void render_child(HDC _dc);
	
	
	bool MouseCheck() const { return m_bMouseOn; }

private:
	void MouseOnCheck();

public:
	// 마우스 클릭 이벤트
	virtual void MouseOn();

	virtual void MouseLbtnDown();
	virtual void MouseLbtnUp();
	virtual void MouseLbtnClicked();

	//CLONE(CUI);
	virtual CUI* Clone() = 0;

public:
	CUI(bool _bCamAff);
	CUI(const CUI& _origin);
	CUI();
	virtual ~CUI();

	friend class CUIMgr;
};
